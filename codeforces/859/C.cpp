int solve(const vi& ss,int i,int j,vvi& memo)
{
	int n=ss.size();
	if(memo[i][j]!=-1) return memo[i][j];
	if(j==n) return memo[i][j]=0;

	if(i==0){
		int x=solve(ss,1,j+1,memo)+ss[j];
		int y=solve(ss,0,j+1,memo);
		return memo[i][j]=max(x,y);
	}
	else{
		int x=solve(ss,0,j+1,memo);
		int y=solve(ss,1,j+1,memo)+ss[j];
		return memo[i][j]=min(x,y);
	}
}

